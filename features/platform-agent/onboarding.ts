export function getOnboardingTemplate(professionHint: string): ProfessionTemplate {
  const normalized = professionHint.trim().toLowerCase();

  // Direct match
  for (const [key, template] of Object.entries(PROFESSION_MAP)) {
    if (normalized.includes(key.toLowerCase())) {
      return template;
    }
  }

  // Alias match
  for (const [alias, canonical] of Object.entries(PROFESSION_ALIASES)) {
    if (normalized.includes(alias.toLowerCase())) {
      const template = PROFESSION_MAP[canonical];
      if (template) return template;
    }
  }

  // Fallback to generic
  return PROFESSION_MAP['ทั่วไป']!;
}
